void PuertaAnd::Dibujar(void)
{
	Puerta::Dibujar();
	if(n > 1)
	{
		/*~~~~~~~~~~~~~~~~*/
		int x = Patita[0].x;
		int y = Patita[0].y;
		/*~~~~~~~~~~~~~~~~*/

		FormR->Image1->Canvas->MoveTo(x, y);
		FormR->Image1->Canvas->LineTo(x, y + (n + 1) * 10);

		// Dibuja las patitas de entrada
		for(int i = 1; i <= n; i++)
		{
			FormR->Image1->Canvas->MoveTo(x - 5, y + 10 * i);
			FormR->Image1->Canvas->LineTo(x, y + 10 * i);
			Patita[i + 1] = Punto(x - 5, y + 10 * i);
		}
	}	// end-if
}
